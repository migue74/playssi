<p>Esta web trata de simular el panel de administración de una cadena de tiendas de videojuegos, 
aunque está reducido debido al tiempo disponible para realizar el trabajo, por lo que es muy 
sencillo.</p>

<p>Las tecnologías usadas han sido XHTML, CSS, PHP y JavaScript. Dentro de esta última, se ha utilizado 
la librería jQuery, muy extendida y casi estandarizada a la hora de trabajar con esta tecnología. Es una librería 
muy rápida y sencilla que permite interactuar con cualquier elemento de la página de forma fácil y en tiempo real.</p>

<p>No se ha utilizado ninguna sentencia "DELETE" puesto que sería un error ya que no se podría guardar un historial. En su lugar, 
se suele trabajar con un campo para indicar si el objeto está activo o no.</p>

<p>El diseño está realizado desde cero, no es ninguna plantilla bajada de la red. Se busca un diseño usable y homogéneo en toda 
la web, a la par que bonito y legible.</p>

<p>La acumulación de exámenes y prácticas no me han permitido pulir cada detalle de la web, por lo que algunas secciones pueden 
parecer un poco vacías. También he de comentar que la he realizado yo solo puesto que vivo a más de 30 KM de Sevilla y me resulta muy 
complicado poder trabajar en grupo en una actividad tan dinámica como esta, donde has de conocer cada línea de tu compañero y resolver 
cada problema que surja preferiblemente en persona.</p>

